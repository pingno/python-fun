# lambda arguments : expression

x = lambda a : a + 10
print(x(5))

# lambda functions can take any number of arguments:

x = lambda a, b : a + b
print(x(5,3)) # output 8




add = lambda a, b : a + b
subtract = lambda a, b : a - b
multiply = lambda a, b : a * b
divide = lambda a, b : a // b

print(add(5, 5))




